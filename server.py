        if phone not in pending_logins: return jsonify({'success': False, 'message': 'انتهت الجلسة'})
        p = pending_logins[phone]; client = p['client']
        async def verify():
            try: await client.sign_in(phone=phone, code=code, phone_code_hash=p['phone_code_hash'])
            except SessionPasswordNeededError:
                if password: await client.sign_in(password=password)
                else: return False, "مطلوب كلمة سر"
            except PhoneCodeInvalidError: return False, "الكود غلط"
            me = await client.get_me(); await client.disconnect()
            user_bot = create_user_bot(p['api_id'], p['api_hash'], phone)
            await user_bot.start(phone=phone)
            active_clients[phone] = user_bot; del pending_logins[phone]
            return True, me.first_name
        loop = asyncio.new_event_loop(); asyncio.set_event_loop(loop); ok, msg = loop.run_until_complete(verify())
